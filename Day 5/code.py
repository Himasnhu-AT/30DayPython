def square_number(x):
    return x * x

def uppercase(text):
    return text.upper()

def half_the_number(x):
    return x / 2

# Get input from user
input_value = int(input())

# Call functions and print results
print(square_number(input_value))
print(uppercase("hello"))
print(half_the_number(input_value))

